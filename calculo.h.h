//Implementação de uma biblioteca

//função para realizar a soma de dois números reais
float soma(float a, float b)
{
	return a + b;
}

//função para realizar a substração de dois números reais
float subtrai(float b, float a)
{
	return a - b;
}

//função para realizar a divisão de dois números reais
float divide(float b, float a)
{
	return a / b;
}

float multiplica(float a, float b)
{
	return a * b;
}
